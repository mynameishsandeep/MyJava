package com.sample.basics;

public class AsciiLocation {
	public static void main(String[] args) {
		String i = "124ab5";
		System.out.println(i.charAt(0) - '0');
		System.out.println(i.charAt(1) - '0');
		System.out.println(i.charAt(2) - '0');
		System.out.println(i.charAt(3) - 'a');
		System.out.println(i.charAt(4) - 'a');
		System.out.println(i.charAt(5) - '0');
	}
}
